package abstract_class_and_method;

/**
 * A eh uma classe abstrata, portanto nao pode 
 * ser instanciada.
 * 
 */
public abstract class A {
    /**
     * m1 eh um metodo abstrato, portanto
     * deve obrigatoriamente ser implementado
     * pelas classes derivadas
     */
    public abstract void m1();
}